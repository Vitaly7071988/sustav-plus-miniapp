async function getAssistantAnswer(question) {
  try {
    const res = await fetch(AI_API_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        question,
        context: {
          day: state.day,
          program: state.program,
          pain: latestPain(),
          waterMl: waterToday(),
          ref: state.ref
        }
      })
    });
    if (!res.ok) throw new Error("AI API unavailable");
    const data = await res.json();
    return data.answer || localAssistantAnswer(question);
  } catch (e) {
    return localAssistantAnswer(question);
  }
}

